public class Dot {
    private double x;
    private double y;
    public Dot (double a_x, double a_y){
        x = a_x;
        y = a_y;
    }
    public double getX(){
        return x;
    }
    public double getY(){
        return y;
    }
    public void setX(double a_x){
        x = a_x;
    }
    public void setY(double a_y){
        y = a_y;
    }
}
